#include "libft.h"

void	ft_putseparation(void)
{
	static int	counter;

	if (counter == 0)
		ft_putstr("-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_\n");
	else if (counter == 1)
		ft_putstr("~^~^~^~^~^~^~^~^~^~^~^~^~^~^~^~^~^~^~^\n");
	else if (counter == 2)
		ft_putstr("^-^-^-^-^-^-^-^-^-^-^-^-^-^-^-^-^-^-^-\n");
	else if (counter == 3)
	{
		ft_putstr("     .-.     .-.     .-.     .-.     ..\n");
		ft_putstr("`._.'   `._.'   `._.'   `._.'   `._.'  \n");
	}
	else if (counter == 4)
		ft_putstr("->->->->->->->->->->->->->->->->->->->-\n");
	else if (counter == 5)
		ft_putstr("+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+\n");
	else if (counter == 6)
		ft_putstr("/^(o.o)^\\ /^(o.o)^\\ /^(o.o)^\\ /^(o.o)^\\\n");
	else
	{
		ft_putstr("<*><*><*><*><*><*><*><*><*><*><*><*><*>\n");
		counter = 0;
	}
	counter++;
}
